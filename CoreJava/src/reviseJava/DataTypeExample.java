package reviseJava;

public class DataTypeExample {
	

	public static void main(String[] args) {
		
		//8 types of data types
		
		/* 1. byte = 1 bit			-128 to 127
		 * 2. short = 2 byte;		-32768 to 32767
		 * 3. int = 4 byte;			
		 * 4. long = 8 byte;
		 * 5. float = 4 byte;
		 * 6. double = 8 byte;
		 * 7. char = 2 byte;
		 * 8. boolean = true or false - 1 bit; 
		
		 */
		
		byte b = 127;
		short s = 32767;
		int i = 123456;
		long l = 123456789;
		float f = 32.345f;
		double d = 3456.456;
		char c = 'A';
		boolean bl = true;

	}

}
